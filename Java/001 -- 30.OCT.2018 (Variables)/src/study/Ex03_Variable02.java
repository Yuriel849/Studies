package study;

public class Ex03_Variable02 {
	public static void main(String args[]) {
		// 1. 자료형 변수이름
		// 변수의 선언과 값의 대입을 분리하는 방법
		int number1;
		number1 = 100;
		
		// 변수의 선언과 값의 대입을 동시에 하는 방법
		int number2 = 100;
			
		// 2. 자료형 변수명1, 변수명2, ...;
		// 여러 변수가 모두 같은 자료형일때만 사용 가능
		int number3 = 30, number4;
			
		// 3. 자료형 변수명1 = 값1, 변수명2 = 값2, ...;
		int number5 = 50, number6 = 60;
		int number7 = 70, number8, number9 = 90;
	}
}